            <h2>Solarstrom rund um die Uhr</h2>
            <p>Etwa jede zweite Solaranlage wird zusammen mit einem Stromspeicher geplant und installiert. Das hat gute Gründe: Den meisten Strom produziert eine Solaranlage in der Mittagszeit, oft mehr, als Sie verbrauchen können. Mit einem Stromspeicher nutzen Sie diesen Strom später, beispielsweise abends zum Kochen und Fernsehen oder zum Aufladen Ihrer elektrischen Geräte über Nacht. </p>
            <p>Anstelle von Strom aus dem öffentlichen Netz nutzen Sie so weiter Ihren eigenen Solarstrom. So erhöht sich Ihr Eigenverbrauch und Ihre Ersparnis. Zusätzlich dazu sind Sie noch unabhängiger vom öffentlichen Netz: Um die 75 % Autarkie sind mit einem Stromspeicher möglich.</p>
