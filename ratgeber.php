            <p>Das <b>Kaufmodell</b> hingegen bietet einen höheren finanziellen Vorteil. Die Lebensdauer einer modernen Solaranlage beträgt in Regel ca. 25 Jahre, weit über den Amortisationszeitraum von ca. 10 Jahren hinaus.</p>
