            <p>Je mehr Ihres Energiebedarfs Sie über selbst erzeugten Strom decken, desto höher die Ersparnis. Schon kleinere Veränderungen im Verbrauchsverhalten können sich lohnen, beispielsweise der Betrieb von Spülmaschine und Waschmaschine zur Mittagszeit, wenn die Solaranlage auf Hochtouren läuft. Jede Kilowattstunde, die sie jetzt nutzen, müssen Sie später nicht für durchschnittlich 29,16 Cent aus dem öffentlichen Netz beziehen (Stand: 07/18).</p>
